void wait ( float seconds )				//시간을 위한 함수.
{
    clock_t endwait;
    endwait = clock () + seconds * CLOCKS_PER_SEC ;
    while (clock() < endwait) {}
}



void Maprint(int su, int out_x, int out_y)		//폭탄피하기 글자 출력함수.
{
	if(out[su][out_y][out_x]==1)	{
		SetColor(0xFC);
		printf("▩");
	}
	else if(out[su][out_y][out_x]==2)	{
		SetColor(0xFE);
		printf("▩");
	}
	else if(out[su][out_y][out_x]==3)	{
		SetColor(0xFA);
		printf("▩");
	}
	else if(out[su][out_y][out_x]==4)	{
		SetColor(0xF9);
		printf("▩");
	}
	else if(out[su][out_y][out_x]==5)	{
		SetColor(0xFD);
		printf("▩");
	}
	else
		printf("  ");
	SetColor(0xF0);
}

void Out(int numb)			//폭탄피하기 글자 출력함수
{
	int outx, outy;
	for(outy=0; outy<OUT_SIZE_Y; outy++)	{
		for(outx=0; outx<OUT_SIZE_X; outx++)		{		
			gotoxy(outx*2,outy);
			Maprint(numb, outx,outy);			
		}
		printf("\n");
	}
}

